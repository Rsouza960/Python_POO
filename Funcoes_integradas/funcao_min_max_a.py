'''
min()-> retorna o menor valor em um iterável
max()-> retorna o maior valor em um iterável
'''

lista = [1,45,78,9,32,67,23]
tupla = (3,56,3,56,23,1)
frase = 'pypro - seja um profissional python!'

print(f'menor lista: {min(lista)} Maior lista: {max(lista)}')
